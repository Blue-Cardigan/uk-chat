function formatDocumentFailures(failures: Array<{ name: string; error: string }>): string {
  if (failures.length === 0) return "";
  const topFailures = failures.slice(0, 3).map((failure) => `${failure.name}: ${failure.error}`);
  const suffix = failures.length > 3 ? ` (+${failures.length - 3} more)` : "";
  return `Some documents could not be used: ${topFailures.join(" | ")}${suffix}`;
}

function formatDocumentWarnings(warnings: ParsedDocumentWarning[]): string {
  if (warnings.length === 0) return "";
  const top = warnings.slice(0, 3).map((w) => `${w.name}: ${w.message}`);
  const suffix = warnings.length > 3 ? ` (+${warnings.length - 3} more)` : "";
  return `Document text was truncated: ${top.join(" | ")}${suffix}`;
}

function isAutoGeneratedTitle(title?: string | null): boolean {
  if (!title) return true;
  return AUTO_CHAT_TITLE_REGEX.test(title.trim());
}

function formatChatErrorMessage(message: string | undefined): string {
  if (!message) return "Something went wrong sending your message. Please try again.";
  try {
    const parsed = JSON.parse(message) as { code?: number | string; message?: string; metadata?: { error_type?: string } };
    if (parsed.code === "MCP_TOKEN_UNAUTHORIZED") {
      return "Your MCP token is no longer valid. Please ask an admin to rotate your token, then refresh and try again.";
    }
    if (parsed.code === 400 || parsed.metadata?.error_type === "invalid_request") {
      return "This model cannot handle that request. Try rephrasing or switching to a different model.";
    }
    if (typeof parsed.message === "string" && parsed.message.trim()) return parsed.message;
  } catch {
    // Not JSON; fall through to the original message.
  }
  return message;
}

function readChatErrorCode(message: string | undefined): string | null {
  if (!message) return null;
  try {
    const parsed = JSON.parse(message) as { code?: unknown };
    return typeof parsed.code === "string" ? parsed.code : null;
  } catch {
    return null;
  }
}

function stableStringify(value: unknown): string {
  if (value == null) return "";
  try {
    return JSON.stringify(value);
  } catch {
    return String(value);
  }
}

function hashString(input: string): string {
  let hash = 0;
  for (let index = 0; index < input.length; index += 1) {
    hash = (hash * 31 + input.charCodeAt(index)) | 0;
  }
  return Math.abs(hash).toString(36);
}

function isOptimisticConversationId(value: string | null | undefined): boolean {
  return typeof value === "string" && value.startsWith(OPTIMISTIC_CHAT_ID_PREFIX);
}

function hasRenderableAssistantPart(part: MessagePart): boolean {
  if (part.type === "step-start") return false;
  if (part.type === "reasoning" || part.type === "tool-invocation" || part.type.startsWith("tool-")) return true;
  if (part.type !== "text") return false;
  const candidate = part as { text?: unknown; content?: unknown; value?: unknown };
  const value = candidate.text ?? candidate.content ?? candidate.value;
  return typeof value === "string" && value.trim().length > 0;
}

